local AceLocale = LibStub and LibStub("AceLocale-3.0", true)

if not AceLocale then
    return
end

local function applyMissing(locale, translations)
    local L = AceLocale:NewLocale(addonName, locale)
    if not L then
        return
    end

    for key, value in pairs(translations) do
        if L[key] == nil then
            L[key] = value
        end
    end
end

